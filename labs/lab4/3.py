# Шаг 1: спрашиваем у пользователя возраст собаки
text = input("Введите возраст собаки (в годах): ")

# Шаг 2: пробуем превратить введённый текст в число
try:
    dog_age = float(text)
except:
    print("Ошибка: введено не число")
else:
    # Шаг 3: проверяем, что возраст в допустимых пределах
    if dog_age < 1:
        print("Ошибка: возраст должен быть не меньше 1")
    elif dog_age > 22:
        print("Ошибка: возраст не должен превышать 22 года")
    else:
        # Шаг 4: считаем человеческий возраст
        if dog_age <= 2:
            # Каждый из первых двух лет = 10.5 человеческих лет
            human_age = dog_age * 10.5
        else:
            # Первые 2 года дают 21 год (10.5 + 10.5)
            # Каждый следующий год — по 4 года
            human_age = 21 + (dog_age - 2) * 4

        # Шаг 5: красиво выводим результат
        if human_age.is_integer():
            human_age = int(human_age)  # убираем .0, если число целое

        print("Возраст собаки в человеческих годах:", human_age)