# Запрашиваем у пользователя первое число и сразу превращаем его в целое число (int)
a = int(input("Введите первое число: "))

# Запрашиваем второе число и тоже делаем его целым
b = int(input("Введите второе число: "))

# Запрашиваем третье число и превращаем в целое
c = int(input("Введите третье число: "))

# Проверяем: если a меньше или равно b И a меньше или равно c,
# значит, a — самое маленькое из трёх
if a <= b and a <= c:
    print("Наименьшее число:", a)

# Если первое условие не сработало (то есть a не самое маленькое),
# проверяем: если b меньше или равно c, то b — самое маленькое из оставшихся
elif b <= c:
    print("Наименьшее число:", b)

# Если ни a, ни b не самые маленькие, значит, самое маленькое — это c
else:
    print("Наименьшее число:", c)