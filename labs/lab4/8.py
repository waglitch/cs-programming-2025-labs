# Вводим сумму покупки
s = float(input("Введите сумму покупки: "))

# Определяем скидку и сразу считаем итог
if s < 1000:
    print("Ваша скидка: 0%")
    print("К оплате:", s)
elif s <= 5000:
    print("Ваша скидка: 5%")
    print("К оплате:", s * 0.95)
elif s <= 10000:
    print("Ваша скидка: 10%")
    print("К оплате:", s * 0.9)
else:
    print("Ваша скидка: 15%")
    print("К оплате:", s * 0.85)